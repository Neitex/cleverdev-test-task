ALTER TABLE patient_note
ADD CONSTRAINT fk_pat_note_to_modified_user FOREIGN KEY (last_modified_by_user_id) REFERENCES company_user(id) ON DELETE NO ACTION;

ALTER TABLE patient_note
ADD CONSTRAINT fk_pat_note_to_patient FOREIGN KEY (patient_id) REFERENCES patient_profile(id) ON DELETE NO ACTION;

ALTER TABLE patient_note
ADD CONSTRAINT fkpat_note_to_created_user FOREIGN KEY (created_by_user_id) REFERENCES company_user(id) ON DELETE NO ACTION;
